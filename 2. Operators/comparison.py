"""
Comparison Operators (output is boolean always)
Used to compare two values

== -> Equal
!= -> Not Equal
> -> Greater Than
< -> Less Than
>= -> Greater Than or Equal To
<= -> Less Than or Equal To
"""

a = 10
b = 10

# Equal
print(a == b)  # True

# Not Equal
print(a != b)  # False

# Greater Than
print(a > b)  # False

# Less Than
print(a < b)  # False

# Greater Than or Equal To
print(a >= b)  # True

# Less Than or Equal To
print(a <= b)  # True
