"""
Arithmetic Operators

+ -> Add
- -> Subtract
* -> Multiply
/ -> Divide
% -> Modulus
** -> Exponent
// -> Floor Division
"""

a = 5
b = 10

# Addition
print(a + b)  # 15

# Subtraction
print(b - a)  # 5

# Multiplication
print(a * b)  # 50

# Division
print(b / a)  # 2.0

# Expoent
print(b**a)  # 25

a = -16
b = 5

print(a % b)

a = -16
b = 5

print(a // b)  # 3
